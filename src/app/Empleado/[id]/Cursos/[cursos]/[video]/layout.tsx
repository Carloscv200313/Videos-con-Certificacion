import { Sidebar } from "@/components/sidebar";
import type { Metadata } from "next";
export const metadata: Metadata = {
    title: "Cursos",
    description: "Generated by create next app",
};
export default async function CursosLayout({
    children,
    params
}: {
    children: React.ReactNode
    params: Promise<{ cursos: string, id: string, video: string }>
}) {
    const { cursos, id, video } = await params
    return (
        <div className="fixed left-0 flex md:flex-row flex-col h-screen w-full">
            <Sidebar idUsuario= {id} idCurso={cursos} idVideo={video} />
            <div className="overflow-y-auto w-full">{children}</div>
        </div>
    )
}